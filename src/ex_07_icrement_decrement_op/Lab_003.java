package ex_07_icrement_decrement_op;

public class Lab_003 {
    public static void main(String[] args) {
        int a= 10;
        System.out.println(++a);
        System.out.println(a);


        // // POST increment  = Print first and then increase
        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);


    }
}
